import json

@app.route('/')
def home():
    # يعرض الصفحة الرئيسية مقسمة لثلاث أقسام: أنبياء، صحابة، مسابقات دينية
    return render_template_string(HOME_TEMPLATE, prophets=PROPHETS_DATA, companions=COMPANIONS_DATA)

@app.route('/prophet/<id>')
def prophet_detail(id):
    # يعرض تفاصيل قصص الأنبياء كاملة بالحرف والرموز التعبيرية
    data = PROPHETS_DATA.get(id)
    if not data:
        abort(404)
    return render_template_string(DETAIL_TEMPLATE, data=data)

@app.route('/companion/<id>')
def companion_detail(id):
    # يعرض تفاصيل سير الصحابة كاملة بالحرف والرموز التعبيرية
    data = COMPANIONS_DATA.get(id)
    if not data:
        abort(404)
    return render_template_string(DETAIL_TEMPLATE, data=data)

@app.route('/quiz')
def quiz():
    # يمرر الأسئلة لصفحة جافا سكريبت التفاعلية كـ JSON
    questions_json = json.dumps(QUIZZES_QUESTIONS, ensure_ascii=False)
    return render_template_string(QUIZ_TEMPLATE, questions_json=questions_json)

if __name__ == '__main__':
    app.run(debug=True, port=5000)

